@app.get("/gating")
def get_gating(
    gsri_score: float = None,
    portfolio_pri: float = None,
    recent_volatility_pct: float = None,
):
    """
    Evaluate dynamic gating level.
    If gsri_score is not provided, it will be calculated live.
    """
    if gsri_score is None:
        gsri_result = calculate_gsri()
        if "error" in gsri_result:
            return {"error": f"Could not calculate GSRI for gating: {gsri_result['error']}"}
        gsri_score = gsri_result["current_gsri"]
    return evaluate_gating(gsri_score, portfolio_pri, recent_volatility_pct)


