@app.get("/run-waterfall-stress-test")
def get_waterfall_stress_test(
    event_magnitude: float = Query(
        default=500_000_000,
        description="Hypothetical loss amount in dollars (e.g., 500000000 for $500M)",
        gt=0,
    ),
    defaulter_initial_margin: float = Query(default=200_000_000, description="Layer 1: Defaulter margin ($)"),
    defaulter_default_fund: float = Query(default=50_000_000, description="Layer 2: Defaulter fund ($)"),
    defaulter_contingent_capital: float = Query(default=30_000_000, description="Layer 3: Contingent capital ($)"),
    mutualized_default_fund: float = Query(default=500_000_000, description="Layer 4: Mutualized fund ($)"),
    srx_clearinghouse_capital: float = Query(default=150_000_000, description="Layer 5: SRX capital ($)"),
):
    """
    Run a clearinghouse stress test by event magnitude.

    This sends a hypothetical loss amount through the 5-layer default
    waterfall and reports the status of each layer.

    Example: /run-waterfall-stress-test?event_magnitude=1000000000

    Example response (truncated):
    {
        "waterfall_result": {
            "loss_amount": 1000000000,
            "total_capacity": 930000000,
            "breach": true,
            "unabsorbed_loss": 70000000,
            "layers": [
                {"layer": 1, "name": "Defaulter Initial Margin",
                 "available": 200000000, "absorbed": 200000000, "status": "EXHAUSTED"},
                ...
            ]
        },
        "event_label": "$1.0B"
    }
    """
    try:
        result = waterfall_stress_test(
            event_magnitude=event_magnitude,
            defaulter_initial_margin=defaulter_initial_margin,
            defaulter_default_fund=defaulter_default_fund,
            defaulter_contingent_capital=defaulter_contingent_capital,
            mutualized_default_fund=mutualized_default_fund,
            srx_clearinghouse_capital=srx_clearinghouse_capital,
        )

        if "error" in result:
            return result

        # Convert the summary DataFrame to a serializable format.
        if "summary_df" in result and hasattr(result["summary_df"], "to_dict"):
            result["summary_df"] = result["summary_df"].to_dict(orient="records")

        return result

    except Exception as error:
        return {"error": f"Waterfall stress test failed: {error}"}
