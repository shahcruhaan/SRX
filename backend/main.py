@app.get("/pri")
def get_pri(
    tickers: str = "SPY,QQQ,TLT,GLD",
    weights: str = None,
    period: str = "2y",
):
    """
    Calculate Portfolio Risk Index.

    Example: /pri?tickers=SPY,TLT,GLD&weights=0.5,0.3,0.2
    """
    ticker_list = _parse_tickers(tickers)
    weight_list = _parse_weights(weights)
    if weights and weight_list is None:
        return {"error": "Weights must be numbers separated by commas. Example: 0.5,0.3,0.2"}
    return calculate_pri(ticker_list, weight_list, period)


