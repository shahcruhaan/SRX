@app.get("/health")
def health_check():
    """
    Detailed health check with component status.

    Returns server uptime, version, and whether each engine module
    is importable and functional.

    Example response:
    {
        "status": "healthy",
        "version": "0.2.0",
        "uptime_seconds": 1234,
        "components": {
            "portfolio_engine": "ok",
            "gsri_engine": "ok",
            "pricing_engine": "ok",
            "stress_engine": "ok",
            "clearinghouse": "ok",
            "gating_engine": "ok",
            "market_data": "ok"
        }
    }
    """
    uptime = round(time.time() - SERVER_START_TIME, 1)
    components = {}

    # Check each module is importable and has its key function.
    checks = [
        ("portfolio_engine", "backend.portfolio_engine", "calculate_pri"),
        ("gsri_engine", "backend.gsri_engine", "calculate_gsri"),
        ("pricing_engine", "backend.pricing_engine", "price_protection"),
        ("stress_engine", "backend.stress_engine", "run_stress_test"),
        ("clearinghouse", "backend.clearinghouse_simulation", "simulate_default_waterfall"),
        ("gating_engine", "backend.gating_engine", "evaluate_srs_gate"),
        ("market_data", "data.market_data", "get_market_prices"),
    ]

    all_ok = True
    for name, module_path, func_name in checks:
        try:
            import importlib
            mod = importlib.import_module(module_path)
            if hasattr(mod, func_name):
                components[name] = "ok"
            else:
                components[name] = f"warning: {func_name} not found"
                all_ok = False
        except Exception as e:
            components[name] = f"error: {e}"
            all_ok = False

    return {
        "status": "healthy" if all_ok else "degraded",
        "version": "0.2.0",
        "uptime_seconds": uptime,
        "components": components,
    }


@app.get("/market-data")
def get_market_data(
    tickers: str = None,
    period: str = "2y",
    data_type: str = Query(
        default="prices",
        description="Type of data: 'prices', 'returns', or 'volume'",
    ),
):
    """
    Get raw market data (prices, returns, or volume).

    Parameters:
        tickers: Comma-separated ticker symbols. If empty, uses SRX core tickers.
        period: Historical period (1y, 2y, 5y).
        data_type: One of "prices", "returns", or "volume".

    Example: /market-data?tickers=SPY,TLT,GLD&data_type=returns&period=1y

    Example response (truncated):
    {
        "data_type": "prices",
        "tickers": ["SPY", "TLT", "GLD"],
        "rows": 502,
        "date_range": {"start": "2023-03-08", "end": "2025-03-07"},
        "latest_values": {"SPY": 567.82, "TLT": 87.34, "GLD": 271.50},
        "data": [{"date": "2023-03-08", "SPY": 380.12, "TLT": 102.5, "GLD": 168.3}, ...]
    }
    """
    try:
        ticker_list = _parse_tickers(tickers) if tickers else None

        if data_type == "prices":
            df = get_market_prices(ticker_list, period)
        elif data_type == "returns":
            df = get_market_returns(ticker_list, period)
        elif data_type == "volume":
            df = get_market_volume(ticker_list, period)
        else:
            return {"error": f"Unknown data_type '{data_type}'. Use 'prices', 'returns', or 'volume'."}

        if df.empty:
            return {"error": "No data returned. Check your tickers and internet connection."}

        # Convert to JSON-friendly format.
        # Format dates safely.
        if hasattr(df.index, "strftime"):
            dates = df.index.strftime("%Y-%m-%d").tolist()
        else:
            dates = [str(d)[:10] for d in df.index]

        records = []
        for i, date in enumerate(dates):
            row = {"date": date}
            for col in df.columns:
                row[col] = round(float(df.iloc[i][col]), 6)
            records.append(row)

        # Latest values.
        latest = {}
        for col in df.columns:
            latest[col] = round(float(df[col].iloc[-1]), 4)

        return {
            "data_type": data_type,
            "tickers": list(df.columns),
            "rows": len(df),
            "date_range": {
                "start": dates[0] if dates else None,
                "end": dates[-1] if dates else None,
            },
            "latest_values": latest,
            "data": records[-10:],  # Last 10 rows (full data would be too large).
            "note": "Only the last 10 rows are returned. Use a local call for full data.",
        }

    except Exception as error:
        return {"error": f"Market data fetch failed: {error}"}


@app.get("/portfolio-risk")
def get_portfolio_risk(
    tickers: str = "SPY,HYG,TLT,GLD,BTC-USD",
    weights: str = "0.40,0.20,0.20,0.10,0.10",
    period: str = "2y",
):
    """
    Combined portfolio risk analysis: PRI + SRS in one call.

    This is a convenience endpoint that runs both calculate_pri() and
    calculate_srs() and returns the results together, saving the
    dashboard from making two separate API calls.

    Example: /portfolio-risk?tickers=SPY,TLT,GLD&weights=0.5,0.3,0.2

    Example response (truncated):
    {
        "pri": { "pri": 42.7, "risk_level": "Moderate", ... },
        "srs": { "scores": { "SPY": { "srs": 48.2, ... }, ... } }
    }
    """
    try:
        ticker_list = _parse_tickers(tickers)
        weight_list = _parse_weights(weights)
        if weights and weight_list is None:
            return {"error": "Weights must be numbers separated by commas."}

        pri_result = calculate_pri(ticker_list, weight_list, period)
        srs_result = calculate_srs(ticker_list, period=period)

        return {
            "pri": pri_result,
            "srs": srs_result,
        }

    except Exception as error:
        return {"error": f"Portfolio risk analysis failed: {error}"}


@app.get("/srs-gsri")
def get_srs_gsri(
    period: str = "2y",
    rolling_window: int = 60,
):
    """
    Combined SRS + GSRI snapshot in one call.

    Returns the current GSRI, current SRS, sub-scores, and gating state
    all in a single response. This saves the dashboard from making
    three separate calls (/gsri, /srs, /gating).

    Example: /srs-gsri?period=1y

    Example response (truncated):
    {
        "gsri": { "current_gsri": 38.4, "risk_level": "Elevated", ... },
        "srs_current": 34.7,
        "gating": { "level": 0, "label": "Normal", ... }
    }
    """
    try:
        gsri_result = calculate_gsri(period=period, rolling_window=rolling_window)

        if "error" in gsri_result:
            return gsri_result

        # Extract SRS from GSRI result.
        srs_current = gsri_result.get("srs_current", 0)

        # Get gating based on the GSRI.
        gating_result = evaluate_gating(gsri_result["current_gsri"])

        return {
            "gsri": gsri_result,
            "srs_current": srs_current,
            "srs_components": gsri_result.get("srs_components", {}),
            "gating": gating_result,
        }

    except Exception as error:
        return {"error": f"SRS/GSRI computation failed: {error}"}


