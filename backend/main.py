class ProtectionRequest(BaseModel):
    """Request body for POST /price-protection."""
    tickers: list = Field(
        default=["SPY", "HYG", "TLT", "GLD", "BTC-USD"],
        description="List of ticker symbols",
        examples=[["SPY", "HYG", "TLT", "GLD", "BTC-USD"]],
    )
    weights: list = Field(
        default=None,
        description="Portfolio weights (must sum to 1.0). If null, equal weight.",
        examples=[[0.40, 0.20, 0.20, 0.10, 0.10]],
    )
    notional: float = Field(
        default=1_000_000,
        description="Portfolio value in dollars",
        ge=1000,
    )
    protection_level_pct: float = Field(
        default=20.0,
        description="Drawdown trigger level (%)",
        gt=0, le=100,
    )
    duration_days: int = Field(
        default=90,
        description="Contract duration in calendar days",
        gt=0,
    )
    period: str = Field(
        default="2y",
        description="Historical data period",
    )


