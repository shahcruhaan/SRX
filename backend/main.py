def _parse_tickers(tickers: str) -> list:
    """Parse a comma-separated ticker string into a clean uppercase list."""
    return [t.strip().upper() for t in tickers.split(",") if t.strip()]


def _parse_weights(weights: str) -> list:
    """Parse a comma-separated weight string into a list of floats. Returns None if empty."""
    if not weights or not weights.strip():
        return None
    try:
        return [float(w.strip()) for w in weights.split(",")]
    except ValueError:
        return None


